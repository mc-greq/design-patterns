package example.patterns.commandpattern.example.workshop;

public class Robot {

    public void turnOn(){
        System.out.println("Turning on the robot!");
    }

    public void cut(){
        System.out.println("Robot is cutting!");
    }

    public void drill(){
        System.out.println("Robot is drilling!");
    }

    public void turnOff(){
        System.out.println("Shutting down the robot!");
    }
}
